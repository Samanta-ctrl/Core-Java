package Hiding;

public class Concept {
/*
 
 	***	Creating a static method in sub class with same signature 
 		as super class static method is known as Method Hiding.
 		
 		* Both parent and child class methods must be static in nature.
 		* Its not overriding concept.
 		* We can only hides static methods but not any non-static methods.
 			
 			
 	*** What happends if both super class and sub class have a field with same name?
 	--> Super class field will be hidden in the sub class.
 
 
 	*** Difference between Method Hiding and Method Overriding in Java?
 	
 	--> 1. In method hiding, both parent and child class methods should be static whereas,
 		   in overriding, both parent and child class methods should be non-static.
 		   
 		2. Compiler is responsible for method resolution based on reference type whereas,
 		   in method overriding, JVM is always responsible for method resolution based on 
 		   runtime object.
 
		3. Method hiding is also known as compile-time polymorphism, static polymorphism,
		   or early binding whereas, method overriding is also known as runtime polymorphism,
 		   dynamic polymorphism, or late binding.
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 */
}
